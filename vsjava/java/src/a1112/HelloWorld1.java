package a1112;

public class HelloWorld1 {
    public static void main(String[] args) {
        System.out.println("'Hello'");
        System.out.println("\"Hello\"");
        // 쌍따옴표를 출력하려면 \ 사용. escape문자형태
        //이스케이프 문자 찾아 문자 적용해보기.
    }
}
// 자주 사용되는 이스케이프 문자
// \\ : 백슬래시 (\) 출력
// \' : 작은따옴표 (') 출력
// \" : 큰따옴표 (") 출력
// \n : 줄 바꿈 (new line)
// \t : 탭 (tab)
// \r : 캐리지 리턴 (carriage return)
// \b : 백스페이스 (backspace)
// \f : 폼 피드 (form feed, 페이지 넘김